#include	"STC8G_H_Delay.h"

//========================================================================
// 函数: void delay_ms(unsigned char ms)
// 描述: 延时函数。
// 参数: ms,要延时的ms数, 这里只支持1~255ms. 自动适应主时钟.
// 返回: none.
// 版本: VER1.0
// 日期: 2021-3-9
// 备注: 
//========================================================================
void delay_ms(unsigned char ms)
{
	unsigned int i;
	do{
		i = MAIN_Fosc / 10000;
		while(--i);
	}while(--ms);
}

/**
 * 延时函数，以秒为单位
 * @param s 延时的时间，单位为秒
 */
void delay_s(unsigned char s) {
    // 计算延时次数，每次延时250毫秒，因此1秒需要延时4次
    unsigned int i = s * 4;
    // 执行延时
    do {
        // 延时250毫秒
        delay_ms(250);
    } while (--i);
}
